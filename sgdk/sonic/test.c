void play() {
    int k;
    YM2612_writeReg( 0, 0x26, 0x0 );
    for( k=0; k<6; k++) {
        int ch = k;
        int wait=0;
        int cont;
                      // 0123456
        char msg[10] = "Canal ";
        msg[6] = 48+k+1;
        msg[7] = 0;
        VDP_drawText( msg, 10, 13);
        if( ch>2 ) ch++;
        for( cont=0; cont<2; cont++ ) {
            YM2612_writeReg( 0, 0x28, 0xf0 | ch );
            for( wait=0; wait<15; wait++ ) {
                YM2612_writeReg( 0, 0x27, 0x2A );
                while( (YM2612_read(0)&3) == 0 );
            }
            YM2612_writeReg( 0, 0x28, 0xf0 | ch );
        }
        YM2612_writeReg( 0, 0x28, ch );
    }
}

int main() {
    Z80_requestBus(1000);
    YM2612_reset();
    patches();
    while(1) play();
    return 0;
}
